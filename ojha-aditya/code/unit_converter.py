'''
This module converts the distance in feet and yard to metre
'''
def yard_to_metre(distance):
    '''
    function to convert distance in yard to SI unit metre
    '''
    return distance*0.9144  # a yard corresponds to 0.9144 metre

def foot_to_metre(distance):
    '''
    function to convert dstance in foot to SI unit metre
    '''
    return distance*0.3048  # a foot corresponds to 0.3048 metre
