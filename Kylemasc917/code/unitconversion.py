"""This is a python module that converts feet and yards to meters"""

def feet_to_meters(feet):
    """A function that converts feet to meters."""

    # the conversion rate of feet to meters is 1 to 0.3048

    return feet * 0.3048

def yards_to_meters(yards):
    """A function that converts yards to meters."""

    # the conversion rate of yards to meters is 1 to 0.9144

    return yards * 0.9144
