"""
This module converts feet and yards to meter
"""

# unit_converter.py

def feet_to_meters(feet):
    """
    Convert feet to meters.
    Parameters:
    feet (float): The distance in feet.
    Returns:
    float: The distance in meters.
    """
    return feet * 0.3048


def yards_to_meters(yards):
    """
    Convert yards to meters.
    Parameters:
    yard (float): The distance in yard.
    Returns:
    float: The distance in meters.
    """
    return yards * 0.9144
