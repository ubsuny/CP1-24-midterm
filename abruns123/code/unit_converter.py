"""
This module contains functions ft_to_m which
converts feet to meters and yd_to_m which 
converts yards to meters
"""

def ft_to_m(ft):
    """
    ft_to_m takes in a value in units of feet 
    and converts it to meters
    """
    return .3048*ft

def yd_to_m(yd):
    """
    yd_to_m takes in a value in units of yards 
    and converts it to meters
    """
    return .9144*yd
